<h2><span style="font-weight: 400;">Merchant Customer Refund Guidelines</span></h2>
<p><span style="font-weight: 400;">KOOFAMILY views food safety the accuracy of all orders made on the KOOFAMILY platform seriously.&nbsp;Not only does it provides customer satisfaction,&nbsp;but at the same time, it solidifies the brand name as a trusted Merchant Partner within the KOOFAMILY platform.&nbsp;This is why KOOFAMILY has been happy to refund customers on behalf of Merchant Partners for a variety of reasons, such as missing or incorrect orders as well as a variety of food-related complaints as stated in the table below.</span></p>
<p><span style="font-weight: 400;">Refunds, however, are just a quick fix. What really matters is minimizing errors in the orders.&nbsp;To achieve this, we need to work together so that a long term solution of reducing customer complaints can be attained and refunds may be kept to a minimum.</span></p>
<p><span style="font-weight: 400;">Starting from 3 August 2020,&nbsp;Koofamiles shall enforce the Customer Refund on all Merchant-Partners following the relevant categories below.&nbsp;Do note that all Merchant-Partners will be given a 4 week grace period before refund is charged. Once the 4 week grace period is up, KOOFAMILY shall deduct a portion of the refunds from the Merchant-Partner&rsquo;s wallet weekly, at the store level, subject to the threshold of orders as determined below.</span></p>
<table>
<tbody>
<tr>
<td>
<p><span style="font-weight: 400;">&nbsp;</span><strong>Thresholds of orders with issues are stated as follows, and are at the store level:</strong></p>
<ol>
<li style="font-weight: 400;"><span style="font-weight: 400;">Complaints exceed 1% of total weekly completed orders, AND</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Number of complaints received exceed the threshold of 3 for the whole week</span></li>
